                </div>
                <input type="text" name="username" class="form-control <?php echo (!empty($username_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $username; ?>" aria-label="Default" aria-describedby="inputGroup-sizing-default">
                <span class="invalid-feedback"><?php echo $username_err; ?></span>
            </div>
            <div class="input-group mb-3">
                <div class="input-group-prepend">
                    <span class="input-group-text" id="inputGroup-sizing-default">Password</span>
                </div>
                <input type="password" name="password" class="form-control <?php echo (!empty($password_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $password; ?>" aria-label="Default" aria-describedby="inputGroup-sizing-default">
                <span class="invalid-feedback"><?php echo $password_err; ?></span>
            </div>
            <div class="form-group">
                <input type="submit" class="btn btn-outline-success" value="Login"/>
            </div>
            <p>Don't have an account? <a href="register.php">Sign up now</a>.</p>
        </form>
